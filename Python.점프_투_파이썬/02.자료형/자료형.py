a = 123
a = -178
a = 0

# 실수형
a = 1.2
a = -3.45

# 8진수, 16진수
# 8진수(Octal)은 0 + (대소문자 o)
a = 0o177

# 16진수(Hexadecimal) 0x
a = 0x8ff
b = 0xABC

# 숫자형을 활용하기 위한 연산자
# 사칙연산( 같음 )

# x의 y제곱을 나타내는 **연산자
# x ** y : x의 y제곱값을 돌려준다.
a = 3
b = 4
a ** b # 81

# % : 나눗셈 후 나머지를 반환 
7 % 3 # 1
3 % 7 # 3

# // : 나눗셈 후 몫을 반환
7 / 4 # 1.75
7 // 4 # 1


